'use strict';

/**
 * Assign tags/labels to texts
 */
class Tagger {
  constructor() {}

  /**
   * Analyse the given text for named entities
   * and return the tag
   * @param {string} text
   * @return Promise<Tag>
   */
  static tagText(text) {
    const res = text;
    return res;
  }
}

module.export = Tagger;
